<p>During the afternoon of Wednesday 30th January 2008
(week
4) the department held a
programming competition in the ITL for all BSc, MSci and MSc students. The purpose
of the
competition was to encourage students to work together, get a feel of
real life and interesting programming challenges, improve their
programming skills, and most
importantly to have fun! </p>
<p>Thirty six students (12 teams of 3 students each) registered for
  the programming challenge (see the <a href="participants.php">list of participants</a>).
  The <a href="rules.php">rules of the competition</a> were simple:  during
  the afternoon, each team was given six problems that they had to try to solve
  within four hours. The winning team successfully solved five problems with
  every team solving at least two problems. Congratulations to all those who
  took part! </p>
